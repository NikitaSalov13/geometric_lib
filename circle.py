import math


def area(r):
    '''Принимает на вход число r, возвращает площадь круга радиуса r (r в квадрате, умноженное на число пи)'''
    return math.pi * r * r

def perimeter(r):
    '''Принимает на вход число r, возвращает длину окружности радиуса r (r умноженное на два и на число пи  )'''
    return 2 * math.pi * r

